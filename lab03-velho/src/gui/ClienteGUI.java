    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nome = jTextField1.getText();
        Cliente c = new Cliente(nome);
        String cpf = jFormattedTextField1.getText();
        int idade = (int) jSpinner1.getValue();
        String rua = jTextField2.getText();
        String n = jTextField3.getText();
        String bairro = jTextField4.getText();
        String cidade = jTextField5.getText();
        String estado = jTextField6.getText();
        String end = rua+","+n+","+bairro+","+cidade+","+estado;
        
        c.setEndereco(end);
        c.setCpf(cpf);
        c.setIdade(idade);
        
        JOptionPane.showMessageDialog(this, "Cliente "+c.getId()+" criada", "Cliente", JOptionPane.INFORMATION_MESSAGE);
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

